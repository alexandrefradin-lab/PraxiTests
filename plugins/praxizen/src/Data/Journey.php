    /**
     * Retourne un seul jour du parcours.
     */
    public static function day(int $day): ?array
    {
        return collect(self::all())->firstWhere('day', $day);
    }

    /**
     * Retourne les jours d'une phase donnée.
     */
    public static function forPhase(string $phase): array
    {
        return collect(self::all())->where('phase', $phase)->values()->all();
    }

    /**
     * Retourne les jours d'une semaine donnée.
     */
    public static function forWeek(int $week): array
    {
        return collect(self::all())->where('week', $week)->values()->all();
    }
