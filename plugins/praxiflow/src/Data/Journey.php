    /**
     * Retourne les jours d'une phase donnée.
     */
    public static function byPhase(string $phase): array
    {
        return array_values(array_filter(self::days(), fn($d) => $d['phase'] === $phase));
    }

    /**
     * Retourne les jours d'une semaine donnée.
     */
    public static function byWeek(int $week): array
    {
        return array_values(array_filter(self::days(), fn($d) => $d['week'] === $week));
    }

    /**
     * Retourne un jour spécifique par son numéro.
     */
    public static function day(int $day): ?array
    {
        foreach (self::days() as $d) {
            if ($d['day'] === $day) return $d;
        }
        return null;
    }

